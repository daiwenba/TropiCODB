		var sub = false;
		
		function toChart(title,subtitle,d){
			var chart = new iChart.Column2D({
				render : 'canvasDiv',
				data : d,
				title : {
					text : title,
					color : '#fffff'
				},
				subtitle : {
					text : subtitle,
					color : '#696969'
				},
				width : 600,
				height : 400,
				border:0,
				label : {
					fontsize:11,
					color : '#666666'
				},
				animation : true,
				animation_duration:700,//700ms完成动画
				shadow : true,
				shadow_blur : 2,
				shadow_color : '#aaaaaa',
				shadow_offsetx : 1,
				shadow_offsety : 0,
				sub_option : {
					listeners : {
						parseText : function(r, t) {
							return t;
						},
						click:function(r,e){
							sub = !sub;
							if(sub)
								toChart(r.get('name')+' sequenced genomes,from Jan 2000 to Now',
										'Annual sequenced genomes',
										data[r.get('name')]);
							else
								toChart('Annual sequenced genomes',
										'from Jan 2000 to Now',
										data.All);
							
						}
					},
					label : {
						fontsize:11,
						fontweight:600,
						color : '#4572a7'
					},
					border : {
						width : 2,
						color : '#ffffff'
					}
				},
				tip:{
					enable:true,
					listeners:{
						parseText:function(tip,name,value,text){
							if(sub)
							return name+'<br/>back';
							else
								if(name=='2000'){
									return name+'<br/>view detials';
								}else{
									return '20'+name+'<br/>view detials';
								}
						}
					}
				},
				coordinate : {
					background_color : 'rgba(240,255,240,0.5)',
					grid_color : '#c0c0c0',
					width : 500,
					axis : {
						color : '#c0d0e0',
						width : [0, 0, 1, 0]
					},
					scale : [{
						position : 'left',
						scale_enable : false,
						label : {
							fontsize:11,
							color : '#666666'
						}
					}]
				}
			});
			
			/**
			 *利用自定义组件构造左侧说明文本。
			 */
			chart.plugin(new iChart.Custom({
					drawFn:function(){
						/**
						 *计算位置
						 */	
						var coo = chart.getCoordinate(),
							x = coo.get('originx'),
							y = coo.get('originy'),
							H = coo.height;
						/**
						 *在左侧的位置，设置逆时针90度的旋转，渲染文字。
						 */
						chart.target.textAlign('center')
						.textBaseline('middle')
						.textFont('600 13px Verdana')
						.fillText('Genome number',x-40,y+H/2,false,'#6d869f', false,false,false,-90);
						
					}
			}));
			
			chart.draw();
		}
